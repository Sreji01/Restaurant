    private void jButtonPromeniSportistuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPromeniSportistuActionPerformed
        try {
            int selektovaniRed = jTableGosti.getSelectedRow();
            if (selektovaniRed == -1) {
                JOptionPane.showMessageDialog(this, "Niste odabrali gosta!", "Upozorenje", JOptionPane.WARNING_MESSAGE);
                return;
            }
            gost = gosti.get(selektovaniRed);
            gost = ClientController.getInstance().pretraziGost(gost);
            JOptionPane.showMessageDialog(this, "Sistem je nasao gosta!", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
            GostForma gf = new GostForma();
            gf.setPgf(this);
            gf.setGost(gost);
            gf.popuniPolja();
            gf.setVisible(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje gosta!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPromeniSportistuActionPerformed

    private void jButtonObrisiSportistuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonObrisiSportistuActionPerformed
        try {
            int selektovaniRed = jTableGosti.getSelectedRow();
            if (selektovaniRed == -1) {
                JOptionPane.showMessageDialog(this, "Niste odabrali gosta!", "Upozorenje", JOptionPane.WARNING_MESSAGE);
                return;
            }
            gost = gosti.get(selektovaniRed);
            gost = ClientController.getInstance().pretraziGost(gost);
            JOptionPane.showMessageDialog(this, "Sistem je nasao gosta!", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
            GostForma gf = new GostForma();
            gf.setPgf(this);
            gf.setGost(gost);
            gf.popuniPolja();
            gf.onemoguciPolja();
            gf.setVisible(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje gosta!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonObrisiSportistuActionPerformed

