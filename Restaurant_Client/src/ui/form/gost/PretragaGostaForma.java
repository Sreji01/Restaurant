    private void jButtonPretraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPretraziActionPerformed
        try {
            if (jComboBoxGradovi.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Morate odabrati grad!", "Upozorenje", JOptionPane.WARNING_MESSAGE);
                return;
            }
            gost = new Gost();
            gost.setGrad((Grad) jComboBoxGradovi.getSelectedItem());
            gosti = ClientController.getInstance().vratiListuGost(gost);
            postaviModelTabeleGosti();
            if (gosti.isEmpty()) {
                throw new Exception();
            }
            JOptionPane.showMessageDialog(this, "Sistem je nasao goste po zadatim kriterijumima!", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da nadje goste po zadatim kriterijumima!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonPretraziActionPerformed

