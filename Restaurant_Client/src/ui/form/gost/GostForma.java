    private void jButtonKreirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonKreirajActionPerformed
        try {
            gost = ClientController.getInstance().kreirajGost(new Gost());
            JOptionPane.showMessageDialog(this, "Sistem je kreirao gosta!", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
            jButtonKreiraj.setEnabled(false);
            jButtonZapamti.setEnabled(true);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da kreira gosta!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonKreirajActionPerformed

    private void jButtonZapamtiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonZapamtiActionPerformed
        try {
            if (jTextFieldIme.getText().equals("") || jTextFieldPrezime.getText().equals("") || jTextFieldBrojTelefona.getText().equals("")
                || jComboBoxGradovi.getSelectedItem() == null) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!", "Upozorenje", JOptionPane.WARNING_MESSAGE);
                return;
            }
            gost.setIme(jTextFieldIme.getText());
            gost.setPrezime(jTextFieldPrezime.getText());
            gost.setBrojTelefona(jTextFieldBrojTelefona.getText());
            gost.setGrad((Grad) jComboBoxGradovi.getSelectedItem());
            ClientController.getInstance().promeniGost(gost);
            if (jButtonKreiraj.isVisible()) {
                isprazniPolja();
                jButtonKreiraj.setEnabled(true);
                jButtonZapamti.setEnabled(false);
            } else {
                pgf.popuniTabeluGostima();
            }
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio gosta!", "Obavestenje!", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Sistem ne moze da zapamti gosta!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonZapamtiActionPerformed

