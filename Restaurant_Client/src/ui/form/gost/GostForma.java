    private void popuniComboBoxGradovima() throws Exception {
        gradovi = ClientController.getInstance().vratiListuSviGrad();
        jComboBoxGradovi.setModel(new DefaultComboBoxModel<>(gradovi.toArray()));
        jComboBoxGradovi.setSelectedItem(null);
    }

    void popuniPolja() {
        jTextFieldIme.setText(gost.getIme());
        jTextFieldPrezime.setText(gost.getPrezime());
        jTextFieldBrojTelefona.setText(gost.getBrojTelefona());
        jComboBoxGradovi.setSelectedItem(gost.getGrad());
        jButtonKreiraj.setVisible(false);
        jButtonObrisi.setVisible(false);
        jButtonZapamti.setVisible(true);
        jButtonZapamti.setEnabled(true);
    }

    void onemoguciPolja() {
        jTextFieldIme.setEditable(false);
        jTextFieldPrezime.setEditable(false);
        jTextFieldBrojTelefona.setEditable(false);
        jComboBoxGradovi.setEnabled(false);
        jButtonKreiraj.setVisible(false);
        jButtonZapamti.setVisible(false);
        jButtonObrisi.setVisible(true);
    }

    private void isprazniPolja() {
        jTextFieldIme.setText("");
        jTextFieldPrezime.setText("");
        jTextFieldBrojTelefona.setText("");
        jComboBoxGradovi.setSelectedItem(null);
    }

